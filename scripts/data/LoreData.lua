------------------------------------------------------------
-- 工具方法
------------------------------------------------------------

function LoreData.getRealmLegend(realmName)
    return LoreData.realmLegends[realmName]
end

function LoreData.getMilestone(capturedCount)
    for i = #LoreData.milestones, 1, -1 do
        if capturedCount >= LoreData.milestones[i].count then
            return LoreData.milestones[i]
        end
    end
    return nil
end

function LoreData.getNewMilestone(oldCount, newCount)
    for _, m in ipairs(LoreData.milestones) do
        if oldCount < m.count and newCount >= m.count then
            return m
        end
    end
    return nil
end

return LoreData
